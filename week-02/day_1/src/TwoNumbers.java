public class TwoNumbers {
    public static void main(String[] args) {
        double a = 13;
        double b = 22;
        System.out.println(a + b);
        System.out.println(b - a);
        System.out.println(b * a);
        System.out.println(b / a);
        int c = Integer.parseInt("22");
        System.out.println(c / a);
        System.out.println(b % a);
    }
}
